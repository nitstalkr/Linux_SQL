    public void cargarDatos(){
        
        this.areasActuales = AreasDao.buscarAreas();
        
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("Nombre");
        modelo.addColumn("Ubicacion");
        this.tablaCont.setModel(modelo);
        for(Area areaAct : this.areasActuales){
            Object toAdd[] = new Object[]{areaAct.getId(),areaAct.getNombre(),areaAct.getUbicacion()};
            modelo.addRow(toAdd);
            this.tablaCont.revalidate();
            this.tablaCont.repaint();
        }
    }
    
    private Area buscarArea(int id){
        for(Area x: areasActuales){
            if(x.getId()==id) return x;
        }
        return null;
    }
    
